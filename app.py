import streamlit as st
import pandas as pd
from datetime import datetime
from streamlit_gsheets import GSheetsConnection

st.set_page_config(page_title="LUMINA SOUL", page_icon="🔮")

# เชื่อมต่อกับ Google Sheets
conn = st.connection("gsheets", type=GSheetsConnection)

st.title("🔮 ระบบถอดรหัสจิตวิญญาณ")
st.subheader("โดย LUMINA SOUL | พื้นที่สะท้อนชีวิต")

name = st.text_input("กรุณากรอก ชื่อ-นามสกุล")
day_born = st.selectbox("คุณเกิดวันอะไร?", ["จันทร์", "อังคาร", "พุธ", "พฤหัสบดี", "ศุกร์", "เสาร์", "อาทิตย์"])

if st.button("🔮 เริ่มการถอดรหัสพลังงาน"):
    if name:
        # 1. แสดงผลคำทำนายทันที
        st.balloons()
        prophecies = {
            "จันทร์": "รหัสทรัพย์จาก 'เมตตา' กุญแจตื่นรู้: รักตัวเองให้เท่ากับรักคนอื่น แล้วเงินจะไหลมาเทมาค่ะ",
            "อังคาร": "รหัสทรัพย์จาก 'แรงขับเคลื่อน' กุญแจตื่นรู้: นิ่งสยบเคลื่อนไหว แล้วจะพบช่องทางรวยใหม่ๆ ค่ะ",
            "พุธ": "รหัสทรัพย์จาก 'การสื่อสาร' กุญแจตื่นรู้: ขอบคุณเงินทุกบาท แล้วพลังงานความมั่งคั่งจะขยายตัวค่ะ",
            "พฤหัสบดี": "รหัสทรัพย์จาก 'ปัญญา' กุญแจตื่นรู้: เชื่อในสัญชาตญาณแรก จักรวาลพร้อมสนับสนุนคุณเสมอค่ะ",
            "ศุกร์": "รหัสทรัพย์จาก 'ความสุข' กุญแจตื่นรู้: ยิ่งมีความสุขจากภายใน เงินจะยิ่งวิ่งเข้าหาคุณค่ะ",
            "เสาร์": "รหัสทรัพย์จาก 'ความอดทน' กุญแจตื่นรู้: กลับมาอยู่กับปัจจุบัน แล้วคุณจะพบขุมทรัพย์ที่ซ่อนอยู่ค่ะ",
            "อาทิตย์": "รหัสทรัพย์จาก 'ผู้นำ' กุญแจตื่นรู้: ลดอัตตาลง เปิดใจรับฟังเสียงภายใน จะมีผู้อุปถัมภ์มาช่วยเปิดทางค่ะ"
        }
        st.success(f"### ผลทำนายสำหรับคุณ {name}")
        st.write(prophecies.get(day_born))
        
        # 2. บันทึกข้อมูลลง Google Sheets
        try:
            new_row = pd.DataFrame([{
                "Timestamp": datetime.now().strftime("%Y-%m-%d %H:%M:%S"),
                "Name": name,
                "Day": day_born
            }])
            # เชื่อมต่อและส่งข้อมูล
            conn.create(data=new_row)
            st.caption("บันทึกรหัสจิตวิญญาณของคุณเรียบร้อยแล้ว ✨")
        except Exception as e:
            # ถ้าบันทึกไม่สำเร็จ ให้รันเงียบๆ ไม่ต้องโชว์ Error ให้ลูกค้าตกใจ
            pass
    else:
        st.warning("⚠️ อย่าลืมใส่ชื่อก่อนนะคะ")
